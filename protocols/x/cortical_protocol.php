Neuromorphometrics has developed a cortical parcellation 
(anatomical labeling) protocol <a href ="http://www.braincolor.org/protocols/docs/BrainCOLOR_cortical_parcellation_protocol.pdf" onClick="return popup(this,'protocol')">
[.pdf]</a>*</font>
that is being used by trained personnel 
to manually label T1-weighted MRI brain volumes as part of two different NIH-funded grants. 
The anatomically labeled brain images will be made publicly available online as a free, 
downloadable resource.
In order to ensure that these anatomical labels are meaningful and useful 
to the neuroscientific community, we conducted a survey about the label definitions.
<br />
<br />
Jason Tourville and Ruth Carper derived the protocol
from some of the most prominent parcellation methods established by other research groups. After a detailed review of these methods, including their anatomic validity and consistency across brains, they consolidated the best approaches into a single parcellation protocol, which currently has 62 cortical regions: 
